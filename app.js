const ENDPOINT = "https://cdn.jsdelivr.net/gh/fawazahmed0/currency-api@1/latest/currencies";

const dropdown_select = document.querySelectorAll(".select-container select");

const submit_btn = document.querySelector("form button");


for(let select of dropdown_select){
    for(currCode in countryList){
        let newOption = document.createElement("option");
        newOption.innerText = currCode;
        newOption.value = currCode;
        if(select.name=="fromoptions" && currCode === "USD"){
            newOption.selected = "selected";
        }
        if(select.name=="tooptions" && currCode === "INR"){
            newOption.selected = "selected";
        }
        select.appendChild(newOption);
    }
    select.addEventListener("change", (e)=>{updateFlag(e.target)});
}


const updateFlag = (elem) => {
    let currCode = elem.value;
    countryCode = countryList[currCode];
    let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png`
    // access parent of elem and then select img with query selector
    const img = elem.parentElement.querySelector("img");
    img.src = newSrc;
}


const updatePrices = () => {
    let amt = document.querySelector("#userinput");
    if(amt.value == '' || amt.value <= 0 || isNaN(parseFloat(amt.value))){
        // alert('Please enter a valid input!'); // disabled for window onload
        // return false;

        amt.value = "1";
    }
        let from = document.querySelector("#fromoptions");
        let to = document.querySelector("#tooptions");


        const urlToHit = `${ENDPOINT}/${from.value.toLowerCase()}/${to.value.toLowerCase()}.json`;
        fetch(urlToHit, {method: 'GET'}).then((res)=>{
            return res.json();
        }).then((resp)=>{
            const resultAmt = parseFloat(resp[to.value.toLowerCase()]) * parseFloat(amt.value);
            let result = document.querySelector(".msg");
            result.innerText = `${parseFloat(amt.value)} ${from.value} = ${resultAmt.toFixed(2)} ${to.value}`
        });
    }

// Event Listeners

window.addEventListener("load", ()=>{
    updatePrices();
});

submit_btn.addEventListener("click", (e)=>{
    e.preventDefault();
    updatePrices();
});